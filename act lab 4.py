score = int(input("Enter your score: "))

if score == 100:
    print("Grade: A")
else:
    if score == 90:
        print("Grade: A")
    else:
        if score == 80:
            print("Grade: B")
        else:
            if score == 70:
                print("Grade: C")
            else:
                if score == 60:
                    print("Grade: D")
                else:
                    print("Grade: F")